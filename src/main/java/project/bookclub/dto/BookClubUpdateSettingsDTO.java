package project.bookclub.dto;

import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

/**
 * 독서모임 정보 수정 요청 DTO
 * POST /bookclubs/{bookClubId}/manage/settings
 *
 * 클라이언트에서 JSON으로 받는 필드:
 * - name: 모임 이름
 * - description: 모임 소개
 * - region: 지역
 * - schedule: 정기 일정
 * - bannerImgUrl: 대표 이미지 URL (1차: URL 입력, 2차: 파일 업로드)
 *
 * 참고:
 * - maxMember(정원)는 수정 불가 (UI/로직에서 제외)
 */
@Data
@NoArgsConstructor
@AllArgsConstructor
public class BookClubUpdateSettingsDTO {
    private String name;         // book_club_name
    private String description;  // book_club_desc
    private String region;       // book_club_rg
    private String schedule;     // book_club_schedule
    private String bannerImgUrl; // banner_img_url (URL 입력)
}
